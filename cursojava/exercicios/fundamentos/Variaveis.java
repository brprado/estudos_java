package fundamentos;

public class Variaveis {

	public static void main(String[] args) {
		//Aprendendo a usar variáveis e constantes e inferência de tipos
		
		//Usando variáveis e constantes
		
		int a = 2; //variável
		System.out.println(a);
		
		final double b = 3.14; //constante
		System.out.println(b);
		
		var olaMundo = "olá, mundo"; //Inferência de tipo
		System.out.println(olaMundo);
		
		char boolT; //Declarando
		boolT = 'T'; //Var inicializada
		System.out.println(boolT); //Usada
		
	}
}
